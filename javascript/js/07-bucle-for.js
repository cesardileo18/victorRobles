// El primer elemento de un bucle for es un contador, que se le suele llamar i, es segundo elemento es una condicion 
// el ultimo elemento que tiene un bucle es el paso o el incrementador o el decrementador
for(var i = 0; i <= numero; i ++){ // se le suma uno a la variable i... si es un sigo menos - se le restaria uno a la variable
    console.log("Vamos por el numero: "+ i);
    // debugger; puedo ver que estÃ¡ pasando dentro del bucle, en source
}